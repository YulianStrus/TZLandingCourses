function updateContent(sectionId, data, indexObj, category) {
  const sectionElement = document.getElementById(sectionId);
  if (!sectionElement) return;
  const item = data[indexObj.value];
  sectionElement.querySelector(".left-column img").src = item.image;
  if (sectionId === "cources") {
    sectionElement.querySelector(".pagination span").textContent = `${
      indexObj.value + 1
    }`;
  } else {
    sectionElement.querySelector(".pagination span").textContent = `${
      indexObj.value + 1
    } / ${data.length}`;
  }

  if (sectionId !== "learn") {
    sectionElement.querySelector(".text-block h2").textContent = category;
    sectionElement.querySelector(".text-block p").textContent =
      item.texts[category];
  }
  updatePagination(sectionId, data.length, indexObj);
}

function updatePagination(sectionId, total, indexObj) {
  const paginationContainer = document.querySelector(
    `#${sectionId} .pagination-numbers`
  );
  if (!paginationContainer) return;
  paginationContainer.innerHTML = "";

  for (let i = 0; i < total; i++) {
    const button = document.createElement("button");
    button.textContent = i + 1;
    button.classList.add("pagination-number");
    if (i === indexObj.value) {
      button.classList.add("active");
    }
    button.addEventListener("click", () => {
      indexObj.value = i;
      updateContent(sectionId, window[sectionId], indexObj, "Web Development");
    });
    paginationContainer.appendChild(button);
  }
}

