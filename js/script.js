function setupTabs(sectionId, data, prevId, nextId, currentIndexObj) {
  const sectionElement = document.getElementById(sectionId);
  if (!sectionElement) return;
  const buttons = sectionElement.querySelectorAll(".tab");
  let currentCategory = "Web Development";

  buttons.forEach((button) => {
    button.addEventListener("click", () => {
      buttons.forEach((btn) => btn.classList.remove("active"));
      button.classList.add("active");
      currentCategory = button.dataset.title;
      updateContent(sectionId, data, currentIndexObj, currentCategory);
    });
  });

  const prevButton = sectionElement.querySelector(`#${prevId}`);
  const nextButton = sectionElement.querySelector(`#${nextId}`);

  if (prevButton) {
    prevButton.addEventListener("click", () => {
      if (currentIndexObj.value > 0) {
        currentIndexObj.value--;
        updateContent(sectionId, data, currentIndexObj, currentCategory);
      }
    });
  }

  if (nextButton) {
    nextButton.addEventListener("click", () => {
      if (currentIndexObj.value < data.length - 1) {
        currentIndexObj.value++;
        updateContent(sectionId, data, currentIndexObj, currentCategory);
      }
    });
  }

  updateContent(sectionId, data, currentIndexObj, currentCategory);
}

